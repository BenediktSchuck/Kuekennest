    <span class="text-3xl min-w-fullfont-semibold">Elternbrief</span>

<p class="my-5 text-lg">Das Spiel hat im Leben von Kindern weder etwas mit zufälliger Freizeitgestaltung noch mit einer rein lustbetonten Tätigkeit zu tun. Das Spiel ist gewissermaßen der Hauptberuf eines jeden Kindes. Es ist dabei die Welt um sich herum, sich selbst, Geschehnisse und Situationen, Beobachtungen und Erlebnisse im wahrsten Sinn des Wortes zu begreifen.
Neben dem Elternhaus als basale Entwicklungssituation, in der die Kinder individuelle und kommunikative Fähigkeiten aufbauen können, ist die Tages-pflege der Ort, an dem das begonnene Fundament gefestigt und ausgebaut wird.
So hat das Spiel Ihrer Kinder grundsätzlich drei Funktionen in sich vereint.
Erstens ist das Spiel eines Kindes von entscheidender Bedeutung für die Persönlichkeitsentwicklung. Zweitens ist das Spiel der Nährboden für einen darauf aufbauenden Erwerb von notwendigen schulischen sowie beruflichen Fähigkeiten. Und drittens gibt es einen deutlichen Zusammenhang zwischen der Spiel- und Schulfähigkeit.
Alles, was Kinder sehen und hören, fühlen, in den Händen halten wird schnell zum Spiel. Ob es das Bauen eines Turmes, das Selbstunterhaltungsspiel beim Buch ansehen, das Aufheben und Werfen eines Steines oder das Klettern auf einen Baum ist. Es ist die „handelnde Auseinandersetzung" der Kinder mit Ihrer gesamten Umwelt. Kinder wollen sie entdecken, verstehen und sich mit unbekannten Dingen vertraut machen. Kinder, die viel und intensiv spielen, nehmen dabei ihre Gefühls- und Gedankenwelt wahr.
Dies möchte ich mit Hilfe meiner pädagogischen Erfahrungen gemeinsam mit ihren Kindern in verschiedenen Spielformen erreichen, zum Beispiel durch Entdeckungs- und Wahrnehmungsspiele, Gestaltungs- und Geschicklichkeitsspiele, Bau- und Steckspiele, Bewegungs- und Musikspiele, Finger- und Handpuppen-spiele.
Mein Ziel ist es, Kindern dabei zu helfen ihre Welt durch Spielen zu entdecken bzw. ihre Spielfähigkeit auf- und auszubauen.</p>

