<p class="text-3xl font-semibold">Zusammenarbeit mit den Eltern</p>

<p class="mt-10 text-lg">Der Austausch mit den Eltern ist einer der Schwerpunkte meiner Arbeit, da ohne Kommunikation eine Betreuung nicht möglich ist.
Ein gutes Verhältnis zu den Eltern, mit denen ich genau bespreche, wie die Betreuung stattfinden soll, so dass beide Seiten zufrieden sind.
Regelmäßige Gespräche, in denen man Rücksprache halten kann, und eventuelle Probleme, die auftauchen können, oder auch positive Ereignisse besprechen kann.
Es ist sehr hilfreich, wenn die Kinder pünktlich gebracht und abgeholt werden (Bitte geben Sie Verspätungen im Voraus bekannt).
Dass eine Übergabe zwischen Eltern und mir als Tagesmutter stattfindet.
Dass es auch mindestens einmal im Jahr zu einem Zusammentreffen der Eltern, Kinder und mir als Tagesmutter kommt (Kleines Fest).
Meine Aufgabe ist es, eine herzliche Ergänzung zum familiären Umfeld der Kinder zu sein.
    Mein Ziel ist es die Kinder in ihrer Eigenständigkeit zu unterstützen.</p>
